describe("COURTS_THAT_CANNOT_BE_RESERVED", () => {
  it("should have canReserve equal to false for all specified court IDs", () => {
    PARKS.forEach((park) => {
      park.courts.forEach((court) => {
        expect(court.canReserve).toBe(
          COURTS_THAT_CANNOT_BE_RESERVED.includes(court.id) ? false : true
        );
      });
    });
  });

  it("should exist as a court", () => {
    COURTS_THAT_CANNOT_BE_RESERVED.forEach((courtId) => {
      const court = courtsById[courtId];
      expect(court.canReserve).toBe(false);
    });
  });
});

describe("COURT_ID_TO_NO", () => {
  it("should have a court number for each court ID", () => {
    PARKS.forEach((park) => {
      park.courts.forEach((court) => {
        expect(COURT_ID_TO_NO[court.id]).toBeDefined();
        expect(court.courtNo).toContain(COURT_ID_TO_NO[court.id]);
      });
    });
  });
});

describe("PARK_TIMES", () => {
  it("should have valid times for each park", () => {
    PARKS.forEach((park) => {
      expect(PARK_TIMES[park.id]).toEqual([park.startTime, park.endTime]);
    });
  });
});

